def split_data(records, train_ratio=0.7, val_ratio=0.15):
    random.shuffle(records)
    n = len(records)
    n_train = int(n * train_ratio)
    n_val = int(n * val_ratio)
    train = records[:n_train]
    val = records[n_train:n_train + n_val]
    test = records[n_train + n_val:]
    return train, val, test


def write_jsonl(path, records):
    with open(path, "w", encoding="utf-8") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")


def main():
    all_records = []
    for t in SCAM_TEXTS:
        all_records.append({"text": t, "label": 1})
    for t in NORMAL_TEXTS:
        all_records.append({"text": t, "label": 0})

    train, val, test = split_data(all_records)

    out_dir = Path("data/demo")
    out_dir.mkdir(parents=True, exist_ok=True)

    write_jsonl(out_dir / "train.jsonl", train)
    write_jsonl(out_dir / "val.jsonl", val)
    write_jsonl(out_dir / "test.jsonl", test)

    print(f"Generated demo dataset at: {out_dir.resolve()}")
    print(f"train={len(train)}, val={len(val)}, test={len(test)}")


if __name__ == "__main__":
    main()
